        <p>Toastmasters International, a world leader in communication and leadership development, is a non-profit organization devoted to making effective oral communication a world-wide reality. Through its member clubs, it helps individuals learn the arts of speaking, listening and thinking - vital skills that promote self-actualization and enhance leadership potential. A Toastmasters club adopts a "learn-by-doing" approach for the same.</p>
<p><b>Toastmasters VIT</b> is one of the most active student chapters in VIT, hosting sessions on all days except for Sundays. With a team of dedicated members, desirous of learning and of helping others learn, the Toastmasters experience helps blossom the internal Leader in all its members. Toastmasters VIT partisans and indulges in a plethora of activities in the University. Toastmasters VIT regularly organizes out-bound training programmes [OBT] for its members at comprising of various group activities that enable the students to learn team-work and leadership abilities.</p>
<p>Toastmasters VIT chapter also organized a national level summit, the Riviera Youth Summit which acted as a curtain-raiser for the inter-collegiate sports and cultural fest, Riviera 2011. The summit provided a constructive platform for helping young minds to channelize their effervescent energy and empowering the youth as well as acting as a guide in the path of moulding a superpower from within the nation.</p>
<p>Toastmasters VIT has also organized workshops on Public speaking and Corporate expectations from young entrepreneurs. Moreover, in Riviera 2011 and Riviera 2012 we showcased our signature event "Mouth Speak" to test the communication skills and the team spirit in an individual. The hallmark event on the Toastmasters International body is the Speech Contest and Toastmasters VIT hosted the club level International Speech Contest and Table Topics Speech Contest that won laudatory admiration and praises of the Area and Division Officers that supervise our club, branding it as one of the most sophisticated events.</p>
<p>Toastmasters VIT then organized the India Emerge Youth Summit 2012 on the theme of Indian policy framework, that acted as a curtain raiser event for the intercollegiate Techo-Management fest, graVITas 2012, hosted by the university. The summit provided a constructive platform for helping young minds to channelize their energy and guided them in the path of making a positive impact in the society around them.</p>
<p>The clubs have made phenomenal progress in the number of educational award holders. There are several Competent Communicator, Competent Leadership and  Advanced Communicator Bronze award holders in the club within just three years of its existence. In addition to this, the clubs have won the coveted "President's Distinguished Club" title bestowed by Toastmasters International headquarters signifying the constructive zeal of the club. The clubs have bagged all of the membership awards - Smedley Award, Talk-up Toastmasters & Beat the Clock through its excellent membership drive.</p>
<p>Clima VIT Toastmasters Club, Brisa VIT Toastmasters Club and Terra VIT Toastmasters Club are also recipients of the Golden Gavel award, adding another feather to TMI VIT's cap. In addition to this, Terra VIT Toastmasters is ranked the 2nd best club in District-82 which consists of over 250 clubs. </p>
<p>The Clubs at Toastmasters VIT are not only active at the Toastmasters International Level, but also is a very active Student's Chapter at VIT University.  Toastmasters VIT has launched four initiatives in the University â€“ Social@VIT, Culturals@VIT, Debate@VIT and Talk@VIT. </p>
<p><b>Social@VIT</b> particularly aims at demolishing the language barrier for the underprivileged children by teaching them English and involving them in various other activities.</p>
<p><b>Cultural@VIT</b> intends on making a few evenings unforgettable for faculty and students by serving as a platform for the brilliant students to showcase their talents. </p>
<p><b>Debate@VIT</b> aims at providing a platform to all those who are willing to speak up, voice their opinions and make a contribution to the society. </p>
<p><b>Talk@VIT</b> is an initiative to endow various skills upon students such as Photography, Photoshop and more. </p>
<p>The Highlight of the year has been that Toastmasters VIT is now officially an Area on the Toastmasters International portal! We have grown from a single club to becoming a very high-performing unit in a short span of time. The Area comprises of the Agua TMI VIT, Fogo TMI VIT, Clima TMI VIT, Terra TMI VIT, Brisa TMI VIT, Trichy club and Selam Club. </p>
