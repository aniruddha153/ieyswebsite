        <p>India is the largest democratic nation in the world. It is essentially a nation for the people, of the people and by the people. Every adult citizen of the country has a right to vote and elect the government of the nation. Unfortunately, today India is viewed as a nation in which the power of democracy is abused. Rampant corruption in governance and bureaucratic systems has led to numerous scams and has brought our country under the international radar for defamatory reasons. Corruption in India has become the major concern of the nation today and this has numerous repercussions. 
The first major scam in India was the Bofors Scam in the 1980s, which is said to be the 'Hallmark of Indian Corruption.' The then Prime Minister Rajiv Gandhi and several other people were accused of receiving payments from Bofors AB for winning a bid to supply India's 155 mm field howitzer. Over the past few years, the number of scams exposed has increased at an alarming rate.</p>

<p>The 2G Spectrum scam, which was exposed in the year 2010, had the then telecommunication minister A. Raja auctioning the communication bandwidth to telecom companies in the year 2008, at the market prices of the year 2001 for personal gain. This scam is said to have cost our country's economy 1,760 billion rupees. Major telecom companies were also a part of this scam. The 2010 Commonwealth Games held in our national capital Delhi, was supposed to be the pride of our nation. But instead, even before the games took off, the grand event was dripping with corruption allegations. It is estimated that out of the 70,000 crores said to have been spent on the game, only half the amount was actually spent on the games. The Central Vigilance Commission, involved in probing the alleged corruption in various Commonwealth Games-related projects, has found discrepancies in tenders- like payment to non-existent parties, willful delays in execution of contracts, over-inflated price and purchase of equipment through tendering and misappropriation of funds.</p>

<p>The Coal Scam exposed in 2012 was undoubtedly India's biggest scam, and worst till date. The CAG (Comptroller and Auditor General) of India, an authority established by the Government of India to audit all the receipts and expenditure by the central and state governments, has accused the Government of India for illegally and irregularly providing our nation's coal deposits to private and public  companies, instead of following the competitive bidding process. The CAG's final report stated that there was a loss of 1,85,591 crore rupees from the national treasury in the period 2004-2009. The harsh truth about this scam is that all major coal dealings during this time period were authorized by the Prime Minister. One of the richest natural resources of our nation has been destroyed by the hands of our corrupt leaders. </p>

<p>In India, the tentacles of corruption are spread far and wide in all horizons of life and not just limited to government organizations. A study by Transparency International  in the year 2008 revealed that more than 62% of Indians had firsthand experience of paying bribes or using internal influence to get work done in public offices. In the year 2012, India was ranked 94 out of 176 countries in Transparency International's Corruption Perception Index.</p>

<p>The tolerance of people towards corruption, lack of intense public outcry, absence of strong public forum opposing corruption are some of the reasons why corruption still plagues the Indian democracy. Media plays a pivotal role in exposing these scams to the public, but the big question is-what next? We know the scams, we know who is behind it, but what we are incognizant of our role as the youth and future of the nation, in extirpating corruption.</p>

<p>We can resist corruption at the rudimentary levels, we can revolt against it through various forums. But ultimately, will this be enough? If not, what can be done? How can we implement Anti-Corruption laws effectively? There are a lot of questions to be asked, numerous issues to be resolved. We intend to do this by bridging the gap between the youth and the leaders on a mega-scale by inviting convergent thinkers and pioneering leaders from all domains to address the issue of corruption and discuss with the audience real-time cases and effective solutions.</p>

<p>Effective youth participation does not mean that they are beheld as passive recipients of national resources, of societal problems. Instead they are stakeholders who play an important role in the country's progress and whose involvement must therefore be appropriately nurtured and cultivated. At IEYS, we aim to do exactly that â€“ channelize the power of the youth to play a pivotal role in the socio-economic generation of the society. We believe in bringing youth to the forefront of global change and innovation because they are the key agents for helping India make a smooth transition from the long held tag of a 'developing nation' to a 'developed nation.' It is an initiative to educate, empower and motivate the youth, for Rabindranath Tagore rightly said- "Age considers, youth ventures".</p>

<p>The theme of IEYS 2013 is 'Corruption and Contemporary India'. It is a platform for leaders across the nation, from various fields, to come together and ignite the young minds of the nation, motivate them to actively participate in combating corruption and helping them visualize the nation as a pool of untapped opportunities. It is time to Realize, Reflect and Rediscover. 
</p>
